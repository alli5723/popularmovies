public class TrailerDetails {
    String key;
    String iso_639_1;
    String name;
    String size;
    String type;

    public String getKey() {
        return key;
    }

    public String getIso_639_1() {
        return iso_639_1;
    }

    public String getName() {
        return name;
    }

    public String getSize() {
        return size;
    }

    public String getType() {
        return type;
    }
}
